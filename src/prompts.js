// Порядок промптов для отображения
const PROMPT_ORDER = ['research', 'researchApply', 'edit', 'editContinue', 'combine', 'title'];

/**
 * Загрузить промпты из localStorage (или вернуть дефолтные)
 */
export function loadPrompts() {
    try {
        const saved = localStorage.getItem(STORAGE_KEY);
        if (saved) {
            const parsed = JSON.parse(saved);
            // Мержим с дефолтными, чтобы новые ключи подтянулись
            const merged = {};
            for (const key of PROMPT_ORDER) {
                merged[key] = parsed[key] || DEFAULT_PROMPTS[key].text;
            }
            return merged;
        }
    } catch (e) {
        console.warn('Failed to load prompts from localStorage:', e);
    }

    // Возвращаем дефолтные тексты
    const defaults = {};
    for (const key of PROMPT_ORDER) {
        defaults[key] = DEFAULT_PROMPTS[key].text;
    }
    return defaults;
}

/**
 * Сохранить промпты в localStorage
 */
export function savePrompts(prompts) {
    try {
        localStorage.setItem(STORAGE_KEY, JSON.stringify(prompts));
        return true;
    } catch (e) {
        console.error('Failed to save prompts:', e);
        return false;
    }
}

/**
 * Получить дефолтные промпты (только тексты)
 */
export function getDefaultPrompts() {
    const defaults = {};
    for (const key of PROMPT_ORDER) {
        defaults[key] = DEFAULT_PROMPTS[key].text;
    }
    return defaults;
}

/**
 * Получить метаданные промптов (labels, descriptions)
 */
export function getPromptMeta() {
    return DEFAULT_PROMPTS;
}

/**
 * Получить порядок промптов
 */
export function getPromptOrder() {
    return PROMPT_ORDER;
}
